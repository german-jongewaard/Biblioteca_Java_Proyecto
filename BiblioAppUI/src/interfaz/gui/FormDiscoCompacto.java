    public void setDiscoCompacto(DiscoCompacto cd) {
        mCD = cd;
        lblTitulo.setText("Editar CD");
        txtNombre.setText(mCD.getTitulo());
        txtGenero.setText(mCD.getGenero());
        txtAño.setText(mCD.getAno());
        txtEdicion.setText(mCD.getEdicion());
        
        if(cd instanceof Audio){
            rbMusica.setSelected(true);
        }else if(cd instanceof Dato){
            rbDatos.setSelected(true);
        }else if(cd instanceof Pelicula){
            rbPeliculas.setSelected(true);
        }
        
        int selected[] = new int[Archivo.getDatos().getCategorias().size()];
        int j = 0;
        for (Categoria c : mCD.getCategorias()) {
            selected[j++] = Archivo.getDatos().getCategorias().indexOf(c);
        }
        lstCategorias.setSelectedIndices(selected);
    }

