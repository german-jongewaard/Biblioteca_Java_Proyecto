    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnAceptarActionPerformed
    {//GEN-HEADEREND:event_btnAceptarActionPerformed
        //Validar datos
        DiscoCompacto cd = null;

        //MUSICA
        if (rbMusica.isSelected()) {

            if (mCD != null) {
                cd = mCD;
                cd.setTitulo(txtNombre.getText());
                cd.setGenero(txtGenero.getText());
                cd.setAno(txtAño.getText());
                cd.setEdicion(txtEdicion.getText());
            } else {
                cd = new Audio(TipoDisco.CD, txtGenero.getText(), txtAño.getText(), txtEdicion.getText(), txtNombre.getText());
                Archivo.getDatos().getItems().add(cd);
            }

            for (Object item : Archivo.getDatos().getCategorias()) {

                cd.borrarCategoria((Categoria) item);

            }

            for (Object item : lstCategorias.getSelectedValuesList()) {
                if (!cd.getCategorias().contains(item)) {
                    cd.agregarCategoria(((Categoria) item));
                }
            }

        } else if (rbDatos.isSelected()) {
            if (mCD != null) {
                cd = mCD;
                cd.setTitulo(txtNombre.getText());
                cd.setGenero(txtGenero.getText());
                cd.setAno(txtAño.getText());
                cd.setEdicion(txtEdicion.getText());
            } else {
                cd = new Dato(TipoDisco.DVD, txtGenero.getText(), txtAño.getText(), txtEdicion.getText(), txtNombre.getText());
                Archivo.getDatos().getItems().add(cd);
            }

            for (Object item : Archivo.getDatos().getCategorias()) {

                cd.borrarCategoria((Categoria) item);

            }

            for (Object item : lstCategorias.getSelectedValuesList()) {
                if (!cd.getCategorias().contains(item)) {
                    cd.agregarCategoria(((Categoria) item));
                }
            }
            //PELICULAS
        } else if (rbPeliculas.isSelected()) {
            if (mCD != null) {
                cd = mCD;
                cd.setTitulo(txtNombre.getText());
                cd.setGenero(txtGenero.getText());
                cd.setAno(txtAño.getText());
                cd.setEdicion(txtEdicion.getText());
            } else {
                cd = new Pelicula(TipoDisco.DVD, txtGenero.getText(), txtAño.getText(), txtEdicion.getText(), txtNombre.getText());
                Archivo.getDatos().getItems().add(cd);
            }

            for (Object item : Archivo.getDatos().getCategorias()) {

                cd.borrarCategoria((Categoria) item);

            }

            for (Object item : lstCategorias.getSelectedValuesList()) {
                if (!cd.getCategorias().contains(item)) {
                    cd.agregarCategoria(((Categoria) item));
                }
            }
        }

        frmParent.refrescar();
        frmParent.cargarCategorias();

        Archivo.guardar();
        this.dispose();

    }//GEN-LAST:event_btnAceptarActionPerformed

